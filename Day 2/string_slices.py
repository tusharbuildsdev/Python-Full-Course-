word = "Python"

# start is always included , stop is always excluded

print(word[0:3])
print(word[2:5])


# you can leave a slide blank to go to very start / end ---
print(word[:3])
print(word[3:])


# negative also work 
print(word[-3:])
print(word[:-2])


#The step 

print(word[::3])

# Python reverse string
print(word[::-1])

print(word[0:10000])
